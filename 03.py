# 03数据类型
'''
python 数据类型:
1.数字类型：整数类型int()、浮点类型float()
2.字符串类型: str()
3.列表类型: list()
4.元组类型: tuple()
5.字典类型: dict()
6.集合类型: set()
7.布尔类型: bool()
8.None类型: None()
'''
a = 10
print(type(a))
b = 10.1
print(type(b))
c = 'hello'
print(type(c))
d = [1,2,3,4,5]
print(type(d))
e = (1,2,3,4,5)
print(type(e))
f = {'name':'zhangsan','age':18}
print(type(f))
g = {1,2,3,4,5}
print(type(g))
h = True
print(type(h))
i = None
print(type(i))
